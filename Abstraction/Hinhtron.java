package Bai1;

public class Hinhtron {
	 private double bk;
	 
	 public double getcanh() {
		 return this.bk;
	 } 
	 public void setcanh(double bk) {
		 this.bk=bk;
	 }
	 public double tinhchuvi() {
		 return bk*2*3.14;
	 }
	 public double tinhdt()
	 {
		 return bk*bk*3.14;
	 }

	}